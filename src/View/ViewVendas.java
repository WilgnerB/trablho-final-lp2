    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTProdutosEstoque.getSelectedRow() != -1 && Integer.parseInt(txtQtd.getText()) > 0) {
            if (salao.podeVender((int) jTProdutosEstoque.getValueAt(jTProdutosEstoque.getSelectedRow(), 2), Integer.parseInt(txtQtd.getText()))) {
                Produto p = tableModelProdutos.getRow(jTProdutosEstoque.getSelectedRow());
                tableModelVenda.addRow(Produto.newinstance(p.getIdprod(), p.getNome(), Integer.parseInt(txtQtd.getText()), p.getPreco(), p.getMarca()));

                txtQtd.setText("");
                txtPreco.setText("");
                txtNome.setText("");
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Quantidade Invalida!");
                txtQtd.setText("");
                txtPreco.setText("");
                txtNome.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum Produto Foi Selecionado Do Estoque , Ou Quantidade Invalida!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTProdutosVenda.getSelectedRow() != -1) {
            tableModelVenda.removeRow((int) jTProdutosVenda.getSelectedRow());
            txtQtd.setText("");
            txtPreco.setText("");
            txtNome.setText("");
            JOptionPane.showMessageDialog(null, "Excluido Com Sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum Produto Foi Selecionado Da Venda!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTProdutosVenda.getSelectedRow() != -1) {
            int x = 0;
            for (int i = 0; i < jTProdutosEstoque.getRowCount(); i++) {
                int colunaQTaValor = (int) jTProdutosEstoque.getValueAt(i, 0);
                if (colunaQTaValor == (int) jTProdutosVenda.getValueAt(jTProdutosVenda.getSelectedRow(), 0)) {
                    x = i;
                    break;
                }
            }
            if (salao.podeVender((int) jTProdutosEstoque.getValueAt(x, 2), Integer.parseInt(txtQtd.getText())) && Integer.parseInt(txtQtd.getText()) > 0) {
                tableModelVenda.setValueAt(txtQtd.getText(), jTProdutosVenda.getSelectedRow(), 2);

                txtQtd.setText("");
                txtPreco.setText("");
                txtNome.setText("");
                JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Quantidade Invalida!");
                txtQtd.setText("");
                txtPreco.setText("");
                txtNome.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum Produto Foi Selecionado Da Venda!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

