    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        List<Produto> produtosestoque = tableModelProdutos.getList();

        List<Produto> produtosvenda = tableModelVenda.getList();

        for (int i = 0; i < produtosvenda.size(); i++) {
            Produto get = produtosvenda.get(i);
            for (int j = 0; j < produtosestoque.size(); j++) {
                Produto teg = produtosestoque.get(j);
                if (get.getIdprod() == teg.getIdprod()) {
                    teg.setQtd(teg.getQtd() - get.getQtd());
                }
            }

        }

        if (salao.createProdutoVenda(produtosvenda)) {
            if (salao.updateProdutos(produtosestoque)) {
                JOptionPane.showMessageDialog(null, "Venda Concluida Com Sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Falha Na Conclusao da Venda!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Falha Na Inserção dos Produtos!");
        }
        dispose();
    }//GEN-LAST:event_jButton4ActionPerformed

